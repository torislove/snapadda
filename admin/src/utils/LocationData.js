// Flat list of all locations for autocomplete search
export const ALL_AP_LOCATIONS = AP_DISTRICTS.flatMap(d => [
  d.hq,
  ...d.mandals
]).filter((v, i, a) => a.indexOf(v) === i).sort();

// District names only
export const AP_DISTRICT_NAMES = AP_DISTRICTS.map(d => d.district);

// Get mandals for a given district name
export function getMandalsForDistrict(districtName) {
  const d = AP_DISTRICTS.find(d => d.district === districtName);
  return d ? d.mandals : [];
}

// Search function: returns locations matching a query string
export function searchLocations(query = '', limit = 10) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase();
  return ALL_AP_LOCATIONS
    .filter(loc => loc.toLowerCase().includes(q))
    .slice(0, limit);
}
